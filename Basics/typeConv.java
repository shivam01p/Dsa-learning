public class typeConv {
    public static void main(String[] args) {
        
        // Type conversion, Implicit type conversion
        char a = 'a';
        char b = 'b';
        int res = a + b;
        System.out.println(res);


        //Type casting, Explicit type conversion
        char re = (char) res;
        System.out.println(re);
    }
}
